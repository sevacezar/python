    text_part = ''
    if command_name == 'find_pet':
        text_part = '–ø–∏—Ç–æ–º—Ü–∞'
    elif command_name == 'find_org':
        text_part = '–æ—Ä–≥–∞–Ω–∏–∑–∞—Ü–∏–∏'
