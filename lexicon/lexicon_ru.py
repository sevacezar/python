def _create_help_text(commands: tuple[tuple]) -> str:
    """
    –§—É–Ω–∫—Ü–∏—è —Ñ–æ—Ä–º–∏—Ä—É–µ—Ç —Ç–µ–∫—Å—Ç –¥–ª—è –∫–æ–º–∞–Ω–¥—ã /help
    """
    text = [f"/{command} - {description}" for command, description in commands]
    return '\n'.join(text)


